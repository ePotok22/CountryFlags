namespace FFF.CountryFlags
{
    /// <summary>
    /// Represents different styles for displaying flags.
    /// </summary>
    public enum FlagStyle
    {
        /// <summary>
        /// Represents a flag displayed in a waving style, suggesting movement.
        /// </summary>
        Waving,

        /// <summary>
        /// Represents a flag displayed in a flat style, with no embellishments or effects.
        /// </summary>
        Flat,

        /// <summary>
        /// Represents a flag that appears shiny, giving it a glossy look.
        /// </summary>
        Shiny,
    }
}
