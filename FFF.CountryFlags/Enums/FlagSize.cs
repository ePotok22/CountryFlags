namespace FFF.CountryFlags
{
    /// <summary>
    /// Represents predefined sizes for flags.
    /// </summary>
    public enum FlagSize
    {
        /// <summary>
        /// Represents a flag size of 16x16 pixels.
        /// </summary>
        s16 = 16,

        /// <summary>
        /// Represents a flag size of 24x24 pixels.
        /// </summary>
        s24 = 24,

        /// <summary>
        /// Represents a flag size of 32x32 pixels.
        /// </summary>
        s32 = 32,

        /// <summary>
        /// Represents a flag size of 48x48 pixels.
        /// </summary>
        s48 = 48,

        /// <summary>
        /// Represents a flag size of 64x64 pixels.
        /// </summary>
        s64 = 64,

        /// <summary>
        /// Represents a flag size of 96x96 pixels.
        /// </summary>
        s96 = 96,

        /// <summary>
        /// Represents a flag size of 128x128 pixels.
        /// </summary>
        s128 = 128,

        /// <summary>
        /// Represents a flag size of 256x256 pixels.
        /// </summary>
        s256 = 256
    }

}
